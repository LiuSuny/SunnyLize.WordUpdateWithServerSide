

namespace SunnyLize.Word
{

    /// <summary>
    /// Styles of page animation for appearing/disappearing
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation take place
        /// </summary>
        None = 0,
        /// <summary>
        /// Page slides in and fade in from the right
        /// </summary>
        SlideAndFadeInFromRight = 1,

        /// <summary>
        /// Page slides out and fade out to the left
        /// </summary>
        SlideAndFadeOutToLeft = 2

    }
}
